public class Q10 {
    public static void main(String[] args) {
        int X = 20, Y = 25;
        System.out.println("The sum of X and Y = "+(X+Y));
        System.out.println("The difference of X and Y = "+(X-Y));
        System.out.println("The product of X and Y = "+(X*Y));
        System.out.println("The average of X and Y = "+(X+Y)/2.0);
        System.out.println("The distance of X and Y = "+(Y-X));
        System.out.println("The minimum between X and Y = "+X);
        System.out.println("The maximum between X and Y = "+Y);
    }
}